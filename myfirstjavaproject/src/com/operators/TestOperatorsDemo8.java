package com.operators;

public class TestOperatorsDemo8 {

	public static void main(String[] args) {
		System.out.println("main method started ");

		int a = 86;
		int b = 3;

		System.out.println(a << b);// Left Shift operator --> 688

		int i = 31;
		int j = 2;
		System.out.println(i >> j);// Right Shift Operators -->
		System.out.println(i >>> j);

	}

}
